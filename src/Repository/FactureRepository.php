    public function findAllWithFournisseur(): array
    {
        $conn = $this->getEntityManager()->getConnection();

        $sql = '
        SELECT p.pack,f.nom,p.created_at,p.file FROM facture p,bon_entrant b,fournisseur f
        WHERE p.pack =b.pack AND f.id = b.fournisseur_id
        GROUP BY p.pack
        ORDER BY b.id DESC
            ';
        $stmt = $conn->prepare($sql);
        $stmt->execute();

        // returns an array of arrays (i.e. a raw data set)
        return $stmt->fetchAll();
    }

